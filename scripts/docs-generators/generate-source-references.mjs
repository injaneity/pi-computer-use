			body: `## macOS\n\nThe macOS helper is indexed by native subsystem: [Accessibility and AX](./macos/accessibility.mdx), [Capture and OCR](./macos/capture.mdx), [Actions and input delivery](./macos/actions.mdx), and the [complete implementation inventory](./macos/implementation.mdx).\n\n## Windows\n\nThe [Windows bridge reference](./windows.mdx) covers UI Automation, capture, input, refs, state, protocol behavior, and constraints.`,
